//Assignment Operators

// =
var a = 123;
console.log("Value assigned to a is " + a);

// += //  a = a + 50  //   Shortcut
var a = 10;
a+=50;
console.log("+= : " + a);

// -= // b = b - 10 
var b = 30;
b-=10;
console.log("-= : " + b);

//Similarly *=, /+, %= are used for same purpose.



